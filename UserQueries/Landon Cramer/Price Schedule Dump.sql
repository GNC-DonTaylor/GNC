--Price Schedule Dump
SELECT 
	WH.IDENTITYID AS WAREHOUSEID, 
	PSH.PRICESCHEDULECODE, PSH.DESCRIPTION, PSH.COMMENT, PSH.EFFECTIVESTARTDATE, 
	--PSH.CHANGEPRICE, PSH.RETAILREQUIRED, PSH.SKUREQUIRED, PSH.USENPC, PSH.APPLYMINFREIGHT, PSH.ALLOWUNSPECIFIED, 
	ZONCOD.CODE, PSD.EXPIRATIONDATE, ITM.ITEMCODE, 
	PSD.RETAILPRICE, PSD.PRICE, PSD.ALTERNATESKU, 
	PSD.DISCOUNTPRICE, PSD.FREIGHTCHARGE, PSD.OTHERCHARGE, PSD.HANDLINGCHARGE, 
	PSD.LANDEDPRICE 

FROM IMPRICESCHEDULEHEADER PSH (NOLOCK) 
JOIN IMPRICESCHEDULEDETAIL PSD (NOLOCK) ON PSD.R_PRICESCHEDULEHEADER = PSH.ROWID 
LEFT JOIN RTZONETABLECODES ZONCOD (NOLOCK) ON ZONCOD.ROWID = PSH.R_ZONE
JOIN IDMASTER WH (NOLOCK) ON WH.ROWID = PSH.R_SHIPPER--PSD.R_WAREHOUSE 
JOIN IMITEM ITM (NOLOCK) ON ITM.ROWID = PSD.R_ITEM 
WHERE PSH.PRICESCHEDULECODE = '60.INTERCO24'
