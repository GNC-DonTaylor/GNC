--10-14750
--10-14751
--10-14752
--10-14753
--10-14754

--Each order has 5 items
--000507.031.1
--000470.010.1
--004380.010.1
--003311.030.1
--008200.070.1

--The 4 price fields are
--Unit Price
--Freight Price
--Handling Price
--Service Charge

SELECT
OH.TRANSACTIONNUMBER,
ITM.ITEMCODE,
OD.UNITPRICECHANGE,
OD.FREIGHTRATEPERITEMCHANGE,
OD.HANDLINGCHARGEPERITEMCHANGE,
OD.TAGGINGCHARGEPERITEMCHANGE
FROM OMTRANSACTIONHEADER OH (NOLOCK)
JOIN OMTRANSACTIONDETAIL OD (NOLOCK) ON OD.R_TRANSACTIONHEADER = OH.ROWID
JOIN IMITEM ITM (NOLOCK) ON ITM.ROWID = OD.R_ITEM
WHERE TRANSACTIONNUMBER IN (
'10-14750',
'10-14751',
'10-14752',
'10-14753',
'10-14754')
